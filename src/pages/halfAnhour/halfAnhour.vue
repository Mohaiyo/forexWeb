		            <p>外汇点值、利润、损失的计算方法 计算外汇点值、利润、损失是开始炒外汇的基础知识，只有明白了这些计算方法，您才能真正的了解和懂得如何下单，如何设计您的外汇交易策略和如何控制风险。</p>
		            <h1>汇对（直盘、非直盘和交叉盘）的计算方法。</h1>
		            <p>•	直盘包括：英镑兑美元、欧元兑美元、澳元兑美元、纽元兑美元 </p>
		            <p>•	非直盘包括：美元兑日元、美元兑瑞郎、美元兑加元 </p>
		            <p>•	交叉盘包括（就是外汇对中没有包括美元）：英镑兑日元、欧元兑日元、澳元兑日元、欧元兑英镑、英镑兑瑞郎</p>
		            <h1>1.直盘</h1>
		            <p> 当美元是为报价货币时，我们称该外汇对为直盘外汇对。一般都是相对于欧元、英镑、纽元和澳元而言。</p>
		            <p>计算直盘外汇对的点值 点差在英文表示为pip (point interest point)，其实就是最小的变化量，也就是基点（tick）。</p>
		            <p>例如欧美的买卖价格为1.2653和1.2652，这样他们的点差就是0.0001，也就是我们说的一个点的点差。</p>
		            <h1>•	直盘点值的计算方法如下： </h1>
		            <p>计算公式：点值=交易手数(lot size) x 基点的数量(tick size) 例如：100000 英美的合约 1点值= 100000 （lot size）x 0.0001 (tick size) = $10美元</p>
		            <p>计算直盘的利润和损失(profit/loss, P/L) 公式如下：</p>
		            <p>卖价 – 买价 = 利润/损失 例如：在1.7505买入200000镑美合约，然后在1.7540时候卖出 1.7540 （卖价）- 1.7505（买价）= 0.0035 = 35个点（利润）转变上面的利润或损失到美元如下： 35（个点）x 200000（手数）x 0.0001（基点）= $700（利润）</p>
		            <h1>•	非直盘 </h1>
		            <p>很多外汇对都是非直盘的外汇对，例如美元兑加元，美元是基本货币，加元是报价货币。当我们说美元兑加元在1.1500时，这个意思是1美元= 1.1500加元</p>
		            <h1>非直盘的点值计算方法 </h1>
		            <p>公式：点值=交易手数(lot size) x 基点(tick size) / 现在汇率 (current rate) </p>
		            <p>例如：当在120.50时购买100000美元兑日元的合约 1点值=100000(lot size) x 0.01(tick size) / 120.50 (current rate) = $8.30</p>
		            <p>计算非直盘的利润和损失 </p>
		            <p>公式： 卖价 – 买价 = 利润 / 损失 </p>
		            <p>例如：在120.50是买入100000 美元兑日元合约，然后再120.30时卖出 120.30 （卖价） - 120.50 （买价）= -0.20 = 20点 （损失） 如何把这个结果转成美元呢，是这样计算的 1点值= 100000（手数）x 0.01（基点）/ 120.30（汇率） = $8.31 所以，$8.31 (点值) x 20（点的损失）= $166.20(损失)</p>
		            <h1>•	交叉盘 </h1>
		            <p>当外汇对中不包括美元的时候，我们称之为交叉盘外汇对。虽然美元没有出现在交叉盘中，不过我们一般也都以美元来计算结果。例如欧元兑英镑，欧元为基本货币，英镑为报价货币。</p>
		            <h1>计算交叉盘外汇对的点值 </h1>
		            <p>公式：点值 = 手数(lot size) x 基点(tick size) x 基本货币与美元的汇率(base quote) / 该外汇的汇</p>
		            <p>率(current rate) </p>
		            <p>例如：在0.6750时买入100000欧元兑英镑的合约，这个时候欧美的汇率为1.1840 1点值 = 100000（手数）x 0.0001（基点）x 1.840（欧美汇率） / 0.6750（欧元兑英镑汇率）= $17.54</p>
		            <p>计算交叉盘的利润和损失 </p>
		            <p>公式：卖价 – 买价 = 利润 / 损失 </p>
		            <p>例如：在0.6760是买入100000欧元兑英镑的合约，然后在0.6750时候卖出</p>
		            <p>0.6760（卖价） - 0.6750（买价） = 0.001 = 10（个点的利润）</p>
		            <p>把这个结果转化成美元：</p>
		            <p> 1点值 = 100000（手数）x 0.0001（基点）x 1.1840（欧美的汇率）/ 0.6750（欧元兑英镑的汇率）=$17.54</p>
		            <p>$17.54 (点值) x 10（个点） = $175.40（利润）</p>
