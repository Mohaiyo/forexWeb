		            <p>有人问，为什么我做多0.1手（10000美元的美元/日元）的美元日元，赚了10个点，为什么获利不是10美元？很多外汇交易者都误认为每个标准手的波动一点就是10美元，0.1手就是1美元，其实并非这样，不同的货币对每个点值可能不同。那么如何计算货币对的点值呢？ </p>
